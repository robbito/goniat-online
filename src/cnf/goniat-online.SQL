DROP TABLE IF EXISTS account ;
DROP TABLE IF EXISTS aut ;
DROP TABLE IF EXISTS bnd ;
DROP TABLE IF EXISTS cat ;
DROP TABLE IF EXISTS geo ;
DROP TABLE IF EXISTS lit ;
DROP TABLE IF EXISTS litloc ;
DROP TABLE IF EXISTS loc ;
DROP TABLE IF EXISTS log ;
DROP TABLE IF EXISTS morpha ;
DROP TABLE IF EXISTS morphb ;
DROP TABLE IF EXISTS tax ;
DROP TABLE IF EXISTS taxlit ;
DROP TABLE IF EXISTS taxloc ;

CREATE TABLE account
(
	AccountId VARCHAR(32) NOT NULL,
	Email VARCHAR(100),
	Password VARCHAR(20),
	User VARCHAR(20),
	Created TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	LastLogin TIMESTAMP DEFAULT NULL,
	Group INTEGER,
	Status INTEGER,
	PRIMARY KEY (AccountId),
	INDEX (User ASC)
)  ;


CREATE TABLE aut
(
	AutId VARCHAR(32) NOT NULL,
	FirstName VARCHAR(30),
	LastName VARCHAR(50),
	Notes TEXT,
	Status INTEGER DEFAULT 0,
	PRIMARY KEY (AutId),
	INDEX (LastName ASC)
)  ;


CREATE TABLE bnd
(
	BndId VARCHAR(32) NOT NULL,
	Code VARCHAR(5),
	MillYears DOUBLE,
	Name VARCHAR(40),
	Notes TEXT,
	Type VARCHAR(4),
	Status INTEGER DEFAULT 0,
	PRIMARY KEY (BndId),
	INDEX (Code ASC),
	INDEX (MillYears ASC),
	INDEX (Name ASC)
)  ;


CREATE TABLE cat
(
	CatId VARCHAR(32) NOT NULL,
	Lft INTEGER DEFAULT 0,
	Name VARCHAR(40),
	Notes TEXT,
	ParentId VARCHAR(32),
	Rgt INTEGER DEFAULT 0,
	Type INTEGER,
	Status INTEGER DEFAULT 0,
	PRIMARY KEY (CatId),
	INDEX (Lft ASC),
	INDEX (Name ASC),
	INDEX (ParentId ASC),
	INDEX (Rgt ASC),
	INDEX (Type ASC)
)  ;


CREATE TABLE geo
(
	GeoId VARCHAR(32) NOT NULL,
	Lft INTEGER DEFAULT 0,
	Name VARCHAR(50),
	Notes TEXT,
	ParentId VARCHAR(32),
	Rgt INTEGER DEFAULT 0,
	Type INTEGER,
	Status INETEGER DEFAULT 0,
	PRIMARY KEY (GeoId),
	INDEX (Lft ASC),
	INDEX (Name ASC),
	INDEX (ParentId ASC),
	INDEX (Rgt ASC),
	INDEX (Type ASC)
)  ;


CREATE TABLE lit
(
	Author1Id VARCHAR(32),
	Author2Id VARCHAR(32),
	Author3Id VARCHAR(32),
	LitId VARCHAR(32) NOT NULL,
	LitNo INTEGER,
	Notes TEXT,
	Reference TEXT,
	Short VARCHAR(30),
	Title TEXT,
	Year VARCHAR(5),
	Status INTEGER DEFAULT 0,
	PRIMARY KEY (LitId),
	UNIQUE (LitNo),
	INDEX (Author1Id ASC),
	INDEX (Author2Id ASC),
	INDEX (Author3Id ASC)
)  ;


CREATE TABLE litloc
(
	LitId VARCHAR(32) NOT NULL,
	LocId VARCHAR(32) NOT NULL,
	Status INTEGER DEFAULT 0,
	INDEX (LitId ASC),
	INDEX (LocId ASC)
)  ;


CREATE TABLE loc
(
	BndLoId VARCHAR(32),
	BndUpId VARCHAR(32),
	Code VARCHAR(6),
	Continent VARCHAR(2),
	GeoId VARCHAR(32),
	LocId VARCHAR(32) NOT NULL,
	LocNo INTEGER,
	Notes TEXT,
	Status INTEGER DEFAULT 0,
	PRIMARY KEY (LocId),
	UNIQUE (LocNo),
	INDEX (GeoId ASC)
)  ;


CREATE TABLE log
(
	AccountId VARCHAR(32),
	LogId VARCHAR(32) NOT NULL,
	RecordId VARCHAR(32),
	OtherId VARCHAR(32),
	Timestamp DATETIME,
	Type INTEGER,
	PRIMARY KEY (LogId),
	INDEX (RecordId ASC),
	INDEX (OtherId ASC),
	INDEX (Timestamp ASC)
)  ;


CREATE TABLE morpha
(
	A21 INTEGER,
	A23 DOUBLE,
	A24 DOUBLE,
	A25 DOUBLE,
	A26 DOUBLE,
	A27 DOUBLE,
	A28 INTEGER,
	A29 VARCHAR(14),
	B35 VARCHAR(10),
	C40 VARCHAR(16),
	D48 VARCHAR(10),
	F53 VARCHAR(8),
	G57 VARCHAR(26),
	G71 VARCHAR(40),
	TaxId VARCHAR(32) NOT NULL,
	Status INTEGER DEFAULT 0,
	PRIMARY KEY (TaxId),
	INDEX (A21 ASC),
	INDEX (TaxId ASC)
)  ;


CREATE TABLE morphb
(
	A22 VARCHAR(28),
	B35 VARCHAR(20),
	C41 VARCHAR(28),
	D49 VARCHAR(16),
	F54 VARCHAR(12),
	TaxId VARCHAR(32) NOT NULL,
	Status INTEGER DEFAULT 0,
	PRIMARY KEY (TaxId),
	INDEX (TaxId ASC)
)  ;


CREATE TABLE tax
(
	AutId VARCHAR(32),
	BndLoId VARCHAR(32),
	BndUpId VARCHAR(32),
	CatId VARCHAR(32) NOT NULL,
	Notes TEXT,
	Pages VARCHAR(20),
	Qualifier INTEGER,
	TaxId VARCHAR(32) NOT NULL,
	TaxNo INTEGER,
	Valid VARCHAR(60),
	Status INTEGER DEFAULT 0,
	PRIMARY KEY (TaxId),
	UNIQUE (TaxNo),
	INDEX (AutId ASC),
	INDEX (CatId ASC),
	INDEX (Qualifier ASC)
)  ;


CREATE TABLE taxlit
(
	LitId VARCHAR(32) NOT NULL,
	TaxId VARCHAR(32) NOT NULL,
	Status INTEGER DEFAULT 0,
	INDEX (LitId ASC),
	INDEX (TaxId ASC)
)  ;


CREATE TABLE taxloc
(
	LocId VARCHAR(32) NOT NULL,
	TaxId VARCHAR(32) NOT NULL,
	Status INTEGER DEFAULT 0,
	INDEX (LocId ASC),
	INDEX (TaxId ASC)
)  ;
